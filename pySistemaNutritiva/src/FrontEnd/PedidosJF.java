    private void jt_precioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jt_precioKeyTyped
        char c = evt.getKeyChar();
        if ( (c != '.') && (c < '0' || c > '9'))
            evt.consume();
    }//GEN-LAST:event_jt_precioKeyTyped

